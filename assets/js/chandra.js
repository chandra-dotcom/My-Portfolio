// Function to handle like button click for a blog card
function handleLikeClick(blogCardId) {
  const likeBtn = document.getElementById(blogCardId).querySelector('.heart-icon');
  const likeCount = document.getElementById(blogCardId).querySelector('#likeCount');

  // Initialize the like count and liked state
  let currentCount = parseInt(likeCount.textContent);
  let isLiked = false;

  // Add a click event listener to the like button
  likeBtn.addEventListener('click', function(event) {
      // Toggle between increasing and decreasing the count
      currentCount = isLiked ? currentCount - 1 : currentCount + 1;

      // Update the like count in the UI
      likeCount.textContent = currentCount;

      // Toggle the 'text-danger' class to change the color
      likeBtn.querySelector('.ti-heart').classList.toggle('text-danger');

      // Update the liked state
      isLiked = !isLiked;

      // Prevent the default behavior only if the href attribute is present
      if (likeBtn.getAttribute('href')) {
          event.preventDefault();
      }
  });
}

// Call the function for each blog card
handleLikeClick('blogCard1');
handleLikeClick('blogCard2');
